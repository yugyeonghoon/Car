package reply;

public class ReplyVO {
	private String rno;
	private String bno;
	private String reply_author;
	private String reply_content;
	private String reply_create_date;
	private String reply_update_date;
	public String getRno() {
		return rno;
	}
	public void setRno(String rno) {
		this.rno = rno;
	}
	public String getBno() {
		return bno;
	}
	public void setBno(String bno) {
		this.bno = bno;
	}
	public String getReply_author() {
		return reply_author;
	}
	public void setReply_author(String reply_author) {
		this.reply_author = reply_author;
	}
	public String getReply_content() {
		return reply_content;
	}
	public void setReply_content(String reply_content) {
		this.reply_content = reply_content;
	}
	public String getReply_create_date() {
		return reply_create_date;
	}
	public void setReply_create_date(String reply_create_date) {
		this.reply_create_date = reply_create_date;
	}
	public String getReply_update_date() {
		return reply_update_date;
	}
	public void setReply_update_date(String reply_update_date) {
		this.reply_update_date = reply_update_date;
	}
	
	
}
